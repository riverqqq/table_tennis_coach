def build_receive_key(
        spin: str,
        length: str,
        placement: str,
        speed: str,
        strength: Optional[str],
) -> str:
    """
    为每一种接发球组合生成唯一 key。
    不转球不带 strength。
    """
    if spin == "no_spin":
        return f"receive__{spin}__{length}__{placement}__{speed}"

    if not strength:
        raise ValueError("非不转球必须提供 strength")

    return f"receive__{spin}__{length}__{placement}__{speed}__{strength}"


def build_receive_label(
        spin: str,
        length: str,
        placement: str,
        speed: str,
        strength: Optional[str],
) -> str:
    """
    生成前端展示用标签。
    """
    parts = [
        SPIN_LABELS[spin],
        LENGTH_LABELS[length],
        PLACEMENT_LABELS[placement],
        SPEED_LABELS[speed],
    ]

    if spin != "no_spin" and strength:
        parts.append(STRENGTH_LABELS[strength])

    return " · ".join(parts)


def build_receive_catalog_skeleton() -> Dict[str, dict]:
    """
    生成 324 种接发球组合的骨架数据。
    说明：
    - 8 种带旋转强弱的旋转 * 3 * 3 * 2 * 2 = 288
    - 1 种不转 * 3 * 3 * 2 = 18
    实际总数是 306。

    你之前按 324 估算，是把“不转”也乘了强弱。
    如果坚持要做成 324，也可以人为给不转保留 weak/strong 两档，
    但从逻辑上更自然的是不转不分强弱。
    """
    catalog: Dict[str, dict] = {}

    spin_with_strength = [
        "topspin",
        "backspin",
        "sidespin_clockwise",
        "reverse_clockwise",
        "side_top",
        "side_bottom",
        "reverse_top",
        "reverse_bottom",
    ]

    for spin in spin_with_strength:
        for length in RECEIVE_LENGTHS:
            for placement in RECEIVE_PLACEMENTS:
                for speed in RECEIVE_SPEEDS:
                    for strength in RECEIVE_STRENGTHS:
                        key = build_receive_key(
                            spin=spin,
                            length=length,
                            placement=placement,
                            speed=speed,
                            strength=strength,
                        )
                        label = build_receive_label(
                            spin=spin,
                            length=length,
                            placement=placement,
                            speed=speed,
                            strength=strength,
                        )

                        catalog[key] = {
                            "key": key,
                            "label": label,
                            "spin": spin,
                            "length": length,
                            "placement": placement,
                            "speed": speed,
                            "strength": strength,
                            "title": f"{label} 的接发球应对",
                            "summary": "待补充",
                            "advice": "待补充",
                            "image_prompts": [],
                        }

    for length in RECEIVE_LENGTHS:
        for placement in RECEIVE_PLACEMENTS:
            for speed in RECEIVE_SPEEDS:
                key = build_receive_key(
                    spin="no_spin",
                    length=length,
                    placement=placement,
                    speed=speed,
                    strength=None,
                )
                label = build_receive_label(
                    spin="no_spin",
                    length=length,
                    placement=placement,
                    speed=speed,
                    strength=None,
                )

                catalog[key] = {
                    "key": key,
                    "label": label,
                    "spin": "no_spin",
                    "length": length,
                    "placement": placement,
                    "speed": speed,
                    "strength": None,
                    "title": f"{label} 的接发球应对",
                    "summary": "待补充",
                    "advice": "待补充",
                    "image_prompts": [],
                }

    return catalog


RECEIVE_CATALOG: Dict[str, dict] = build_receive_catalog_skeleton()

